class Solution {
public:
  bool isAnagram(string s, string t) {
    if (s.size() != t.size())
      return false;

    int arr[26] = {0};

    for (int i = 0; i < s.size(); ++i) {
      ++arr[s[i] - 'a'];
      --arr[t[i] - 'a'];
    }

    // Print
    for (int l = 0; l < 26; ++l)
      cout << char(l + 'a') << ":" << arr[l] << ", ";
    cout << endl;

    for (int &a : arr)
      if (a)
        return false;

    return true;
  }
};

