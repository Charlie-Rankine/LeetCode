class Solution {
public:
  int numSubarraysWithSum(vector<int> &nums, int goal) {
    int ans = 0;

    for (int L = 0, R = 0, total = 0, tail = 0; R < nums.size(); ++R) {
      total += nums[R];

      if (nums[R])
        tail = 0;

      while (L <= R && total >= goal) {
        if (total == goal)
          ++tail;

        total -= nums[L++];
      }

      ans += tail;
    }

    return ans;
  }
};

