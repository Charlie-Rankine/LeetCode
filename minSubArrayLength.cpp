#include <algorithm>
#include <iostream>
#include <vector>

using namespace std;

class Solution {
public:
  int minSubArrayLen(int target, vector<int> &nums) {
    int smallest = 2e9, L = 0, R = 0, total = 0;

    while (R < nums.size()) {
      total += nums[R++];

      while (total >= target) {
        smallest = min(smallest, R - L);
        total -= nums[L++];
      }
    }

    return (smallest == 2e9) ? 0 : smallest;
  }
};

